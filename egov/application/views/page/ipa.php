                    <h4>ILMU PENGETAHUAN ALAM</h4>
                    <p class="mb-4 pb-2" style="text-align: justify;">Program MIPA (Matematika dan IPA) merupakan salah satu jurusan yang ada di SMA N 3 Sumedang. 
                    Secara garis besar Program MIPA itu singkatan dari Matematika dan Ilmu Pengetahuan Alam. 
                    IPA juga biasanya dikenal dengan ilmu sains. Dalam bahasa Latin, kata sains berasal dari scientia yang mempunyai arti pengetahuan. yang akan mempelajari tentang ilmu eksak seperti matematikan, kimia, fisika, dan biologi. 
                    jurusan MIPA mempelajari gejala alam dengan hukum yang pasti, cara kerja alam, ekosistemnya, dan masih banyak lagi.</p>
                    <h4>Visi</h4>
                    <p class="mb-4 pb-2" style="text-align: justify;">Menghasilkan lulusan yang mampu mempraktikkan ilmu matematika dan ilmu pengetahuan tentang alam sebagai bentuk kemajuan kehidupan dunia science.</p>
                    <h4>Misi</h4>
                    <p class="mb-4 pb-2" style="text-align: justify;">Program Studi Ilmu Pengetahuan Alam mengembangkan misi sebagai berikut.
                    <p>1.   Menyelenggarakan sistem pembelajaran berbasis Ilmu Pengetahuan Alam;</p>
                    <p>2.   Menerapkan kurikulum merdeka berkaitan dengan science;</p>
                    <p>3.	Menerapkan sistem pembelajaran yang mengutamakan perhitungan, pengetahuan, dan sikap yang kompeten</p>
                    <p>4.	Memberikan kesempatan yang luas kepada siswa untuk mengembangkan kreatifitasnya sesuai dengan kompetensi yang dipilih dalam bidang matematika dan ilmu pengetahuan alam;</p>
                    <p>5.	Memberikan kesempatan yang besar kepada siswa untuk dapat melakukan praktik dengan baik dan benar sesuai dengan SOP.</p>
                <P>Muatan Jurusan MIPA (Matematika dan IPA) di SMAN 3 Sumedang mempelajari Apa Saja Materinya?</P>
                <h4>Matematika</h4>
                <Pclass="mb-4 pb-2" style="text-align: justify;">Saat memasuki jurusan IPA, anda akan menerima mata pelajaran Matematika yang dibagi menjadi dua yaitu Matematika Wajib sekaligus Matematika Peminatan. Matematika Peminatan berbeda dengan Matematika Wajib karena mata pelajaran SMA jurusan IPA kelas 10 yang satu ini lebih sulit. Mata pelajaran matematika anda akan mempelajari terkait Persamaan Linier Dua/Tiga Variabel, Aljabar, Algoritma dan Matematika Peminatan kelas anda akan belajar materi-materi seperti logaritma, grafik, persamaan, hingga skalar dan vektor.</P>
                <h4>Fisika</h4>
                <Pclass="mb-4 pb-2" style="text-align: justify;">Saat memasuki kelas 10, anda juga akan menerima mata pelajaran fisika. Di mata pelajaran IPA yang satu ini anda akan mendapat materi mulai dari hukum Newton, gerak lurus, besaran dan satuan, gerak parabola, hingga usaha dan energi.</P>
                <h4>Kimia</h4>
                <Pclass="mb-4 pb-2" style="text-align: justify;">Selain belajar sistem periodik, mata pelajaran SMA jurusan IPA yang satu ini juga akan membahas materi lainnya, seperti struktur atom, larutan elektrolit dan non elektrolit, hingga ikatan kimia.</P>
                <h4>Biologi</h4>
                <Pclass="mb-4 pb-2" style="text-align: justify;">Biologi adalah salah satu mata pelajaran yang bakal dipelajari di jurusan IPA. Pelajaran ini mempelajari tentang makhluk hidup, mulai dari manusia, hewan hingga tumbuhan, mulai dari klasifikasi makhluk hidup, virus, ekologi, fungi/jamur, dan masih banyak lagi.</P>
                <Pclass="mb-4 pb-2" style="text-align: justify;">Berikut adalah Sarana dan prasarana Praktikum penunjang:</p>
                <p>1.	Laboratorium yang terdiri dari Lab Fisika, Lab Kimia, dan Lab Biologi.</p>
                <p>2.	Peralatan Praktikum pendukung yang lengkap dan berkualitas sesuai dengan perkembangan zaman. </p>
                <p>3.	APD praktikum yang telah sesuai dengan aturan P3K</p>
                <p>4.	Jas Lab</p>
                <p>5.	Ruang sterilisasi</p>
